		もしどうしてもEXCELを使う場合は、データ＞外部データの取り込み＞テキストファイル　でインポートして、ＣＳＶ形式で保存してみてください。</p>


	<a name="FTP自動転送（商品CSV転送・画像自動取得＆転送）について" id="FTP自動転送（商品CSV転送・画像自動取得＆転送）について"></a>
	<h1 id="faq_title">　FTP自動転送（商品CSV転送・画像自動取得＆転送）について</h1>
		<p class="q">楽天画像自動転送の仕組みを教えてください。</p>
		<p class="a">「8.楽天画像を転送開始」をクリックしますと、商品画像（いわゆるカゴ画像）はもちろんのこと、取り込んだＣＳＶ内のhtmlに記載されている画像URLをもとにすべての画像を自動取得し、そのままノンストップでヤフーのストアマネージャーに転送されるようになっております。<br>
  		さらに商品画像についてはヤフーの仕様に合わせて自動リネームしてくれます。<br>
		※この機能は有償機能となっております。<br>
		<a href="https://mallassist-a.com/entry">＞＞有償機能はこちらからお申し込みください</a></p>


		<p class="q">商品画像以外の画像はヤフーの別フォルダに登録されるのですか？</p>
		<p class="a">商品画像（カゴ画像）以外は、追加画像フォルダに格納されます。</p>

		<p class="q">goldの画像も取得できるということはトップページのバナーとかも取得できるのですか？</p>
		<p class="a">商品ＣＳＶに記載されている画像URLのみ取得します。商品説明文等にその画像のURLが埋め込まれていれば取得できます。</p>

		<p class="q">画像の容量制限が心配なので、不要な画像は転送しないような指定ができませんか？</p>
		<p class="a">画像を自動取得しそのままノンストップでヤフーのストアマネージャーに転送されるようになっておりますので、転送する画像を選択することはできません。もし不要な画像があれば、転送後にストアマネージャーにて削除してください。</p>

		<p class="q">楽天から取得した画像をローカルにダウンロードできませんか？</p>
		<p class="a">画像を自動取得しそのままノンストップでヤフーのストアマネージャーに転送されるようになっておりますので、ストアコンバーターから画像をダウンロードすることはできません。</p>

		<p class="q">トリプルに画像を転送できますか？</p>
		<p class="a">現在はトリプルへの画像転送には対応しておりません。<br><br>
		裏ワザとして以下のようなやり方もできるかと思いますので参考までにご検討ください。<br>
		-------------------------------------------------------------------------------- <br>
		1)「1.環境設定」＞ヤフーFTPサーバの情報　にトリプルのFTPサーバを登録する。 <br><br>
		2)「8.楽天画像を転送開始」を実行する。 <br><br>
		3)この後はいつもお使いのFTPソフトでの作業となります。<br>
		トリプルのルートフォルダに転送されたZIPをすぐにダウンロードし、すぐにトリプルから削除します。<br>
		※ZIPはサイズや種類によって複数生成される可能性があり、ルートフォルダ上に一つでもZIPがあれば次のZIPは転送されない仕組みになっているので、一つ転送されたらすぐにダウンロードしてすぐにトリプルから削除するということを繰り返します。 <br><br>
		4)ダウンロードしたzipを解凍して、あらためてトリプルにFTPアップロードします。 <br>
		-------------------------------------------------------------------------------- <br>
		ただしこの方法につきましては実際に検証したわけではありませんので動作保障はできかねますことをあらかじめご了承ください。</p>

		<p class="q">ヤフーのFTP利用申請をしましたが使えるようになるまで３週間かかるようです。<br>
		その間ストアマネージャの画像管理からアップロードして対応したいのですが、楽天から取得した画像をローカルにダウンロードできませんか？</p>
		<p class="a">ストアコンバーターでは、画像を取得しFTP転送までノンストップで行う仕様となっておりますので、残念ながらローカルにダウンロードすることはできません。 <br><br>
		裏ワザとして以下のようなやり方もできるかと思いますので参考までにご検討ください。<br>
		-------------------------------------------------------------------------------- <br>
		1)「1.環境設定」＞ヤフーFTPサーバの情報　にお手持ちのFTPサーバを登録する。 <br><br>
		2)「8.楽天画像を転送開始」を実行する。 <br><br>
		3)この後はいつもお使いのFTPソフトでの作業となります。<br>
		FTPサーバのルートフォルダに転送されたZIPをすぐにダウンロードし、すぐにFTPサーバから削除します。<br>
		※ZIPはサイズや種類によって複数生成される可能性があり、ルートフォルダ上に一つでもZIPがあれば次のZIPは転送されない仕組みになっているので、一つ転送されたらすぐにダウンロードしてすぐにFTPサーバから削除するということを繰り返します。<br><br>
		4)ダウンロードしたzipをストアマネージャ内からアップロードします。 <br>
		-------------------------------------------------------------------------------- <br>
		ただしこの方法につきましては実際に検証したわけではありませんので動作保障はできかねますことをあらかじめご了承ください。</p>

		<p class="q">ストアマネージャーから追加画像をZIP形式でアップロードする場合、登録するカテゴリーフォルダを選択しなければならないようですがどうすればよいのでしょうか？</p>
		<p class="a">ヤフーの追加画像につきましては、どのカテゴリフォルダに保存したとしても画像URLは同じになります。「/lib/ストアアカウント/画像ファイル名．拡張子」です。<br>
		従って一つカテゴリフォルダを作成してそこに全部保存するということでも良いかと思います。詳しくはヤフーのサポートにお問合せください。</p>


	<a name="ヤフーにアップロードまたは転送した時に出るエラーについて" id="ヤフーにアップロードまたは転送した時に出るエラーについて"></a>
	<h1 id="faq_title">　ヤフーにアップロードまたは転送した時に出るエラーについて</h1>
		<p class="q">「オプションのフォーマットが無効です」となってしまいました。</p>
		<p class="a">optionに禁止文字（半角記号の|;:&=#"\）が含まれている可能性があります。</p>

		<p class="q">文字数は合っているのに「○○は○○文字以内で入力してください」となってしまいました。</p>
		<p class="a">html不可の項目にhtmlが書かれている可能性があります。</p>

		<p class="q">「オプション値が重複しています（アルファベットの大文字/小文字は区別しません）」となってしまいました。</p>
		<p class="a">楽天の項目選択肢別在庫用横軸選択肢か項目選択肢別在庫用縦軸選択肢の値に空白が含まれている場合、空白で区切られて別々のオプション値と判断されてしまいます。<br>
		例）楽天の項目選択肢が「メンズ Ｌ」「メンズ Ｍ」の場合、ヤフーでは「メンズ」「Ｌ」「メンズ」「Ｍ」とみなされ「メンズ」が重複したというエラーになってしまう。<br><br>
		ストアコンバーターに取込む前に、項目選択肢から空白を取り除いておくことが、エラーにならないコツです。</p>

		<p class="q">「オプションで入力された選択肢と一致しません」となってしまいました。</p>
		<p class="a">上記同様、項目選択肢内の空白のため、sub-codeとoptionが不一致を起こしている可能性があります。ストアコンバーターに取込む前に、項目選択肢から空白を取り除いておくことが、エラーにならないコツです。</p>

