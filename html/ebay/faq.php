		<p class="q">プロダクトカテゴリの変換はどういう仕組みか教えてください。
		他社製品では、紐付けを自分で設定しないと変換できないと言われました。</p>
		<p class="a">ストアコンバーターは紐付のデータベースを持っているのでご自身で設定しなくても変換されます。<br>
		また、変換結果を確認して紐付けを変更することもできます。<br>
		※この機能は有償機能となっております。無償版ではプロダクトカテゴリIDは空欄となります。<br>
		<a href="https://mallassist-a.com/entry">＞＞有償機能はこちらからお申し込みください</a></p>

		<p class="q">モール側でプロダクトカテゴリが変更になったらどうなるのですか？</p>
		<p class="a">弊社は楽天もヤフーもパートナー企業になっておりますので、情報が入り次第データベースは随時変更いたします。</p>

		<p class="q">「123456:現在有効なディレクトリIDではありません」と表示されたのですがどうすれば良いですか？</p>
		<p class="a">楽天のCSVに登録されている123456というディレクトリIDが現在無効なIDだった場合、紐付くヤフーのプロダクトカテゴリIDが見つからないのでこのように表示されます。<br>
		その場合はヤフーの<a href="https://mallassist-a.com/mypage/pc_list">プロダクトカテゴリ一覧</a>をご参照いただきプロダクトカテゴリIDを入力してください。</p>

