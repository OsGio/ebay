		<p class="q">楽天のPC用説明文がヤフーのcaptionにセットされているようですが、abstractに変更することは可能ですか？</p>
		<p class="a">変換の仕様は固定となっています。操作方法画面をご参照ください。abstractは文字数制限が半角1,000文字と小さいため、PC用説明文はcaptionにセットする仕様となっています。</p>

		<p class="q">商品ＣＳＶを変換したらそのままヤフーにアップロードして良いですか？</p>
		<p class="a">変換ログを参照してください。禁止タグ使用や文字数オーバーなどのエラー情報が表示されていますので「6.変換データ一括編集」で削除や置換を行ってください。<br>
		一括編集が終わったらＣＳＶをダウンロードして、必要に応じ個別に編集してからヤフーにアップロードしてください。</p>

		<p class="q">データ変換を実行すると、楽天の画像URLはヤフーの画像URLに自動変換してくれるのですか？</p>
		<p class="a">画像のURLはご自身で直していただくかたちとなります。 「6.変換データ一括編集」で編集することができます。 </p>


	<a name="変換ログ参照について" id="変換ログ参照について"></a>
	<h1 id="faq_title">　変換ログ参照について</h1>
		<p class="q">ログ内容の見かたを教えてください。</p>
		<p class="a">例）additional1　商品情報が長すぎます。 LEN:10162＞10000[XXXXXXXX]<br><br>
		これは、XXXXXXXXという商品番号について、ヤフーのadditional1という項目の文字数制限が半角10000文字なのに対して、楽天の文字数が10162文字だったということを表しています。<br>
		このままヤフーにアップロードするとエラーになります。162文字分削除する必要があります。</p>

		<p class="q">「在庫情報がない商品があります」と出るのですがどういう意味ですか？</p>
		<p class="a">楽天にて在庫タイプを「2：項目選択肢別在庫設定」と設定しているのに項目選択肢別在庫情報が無い場合にこのログが出力されます。</p>

	<a name="変換データ一括編集について" id="変換データ一括編集について"></a>
	<h1 id="faq_title">　変換データ一括編集について</h1>
		<p class="q">楽天の画像URLをヤフーの画像URLに置換するやり方を教えてください。</p>
		<p class="a">画像のURLは「6.変換データ一括編集」で、全商品について一括置換することができます。<br><br>
		　例） <br>
		　楽天のcabinetをaaaaa、bbbbb、cccccというようにフォルダで分けている場合は、下記のように複数回に分けて置換します。 <br><br>
		　１回目 <br>
		　　対象文字列：＜IMG src="http://image.rakuten.co.jp/店舗URL/cabinet/aaaaa/ <br>
		　　置換文字列：＜IMG SRC="/lib/ストアアカウント/ <br><br>
		　２回目 <br>
		　　対象文字列：＜IMG src="http://image.rakuten.co.jp/店舗URL/cabinet/bbbbb/<br>
		　　置換文字列：＜IMG SRC="/lib/ストアアカウント/ <br><br>
		　３回目 <br>
		　　対象文字列：＜IMG src="http://image.rakuten.co.jp/店舗URL/cabinet/ccccc/ <br>
		　　置換文字列：＜IMG SRC="/lib/ストアアカウント/ <br><br>
		参考）ヤフーの追加画像のURLは「/lib/ストアアカウント/画像ファイル名．拡張子」となります。<br>
		追加画像フォルダ内にカテゴリフォルダを作成しても、URLは変更ありません。</p>

		<p class="q">楽天の商品画像URL（カゴ画像URL）は置換しなくて良いのですか。</p>
		<p class="a">ヤフーの商品CSVには商品画像URLの項目が存在しませんので、置換は不要です。</p>


